    private void fin(){
        if (timer != null) timer.stop();

        PantallaFinal pf = new PantallaFinal(puntuacion); 
        pf.setVisible(true);
        this.dispose();
        }
    
    private void moverBoton() {
        if (pause) {
            return; 
        }

        int factorAumento = puntuacion / 10;

        //Velocidad actual y límite máximo
        int velocidadDinamica = velocidadBase + factorAumento;
        int velocidadActual = Math.min(velocidadDinamica, velocidadMaxima);

        //Pasos (paso = dirección * velocidad)
        int pasoX = dirX * velocidadActual;
        int pasoY = dirY * velocidadActual;

        //Mover
        int x = jBclick.getX();
        int y = jBclick.getY();
        int nuevoX = x + pasoX;
        int nuevoY = y + pasoY;

        //Colisiones con el borde
        int anchoVentana = getContentPane().getWidth();
        int altoVentana = getContentPane().getHeight();
        int anchoBoton = jBclick.getWidth();
        int altoBoton = jBclick.getHeight();

        
        //Lógica de Rebote (Colisiones)

        // Rebote Horizontal (Ejes X)
        if (nuevoX + anchoBoton > anchoVentana || nuevoX < 0) {
            dirX = -dirX; // Invierte la dirección
            nuevoX = x + dirX * velocidadActual; 
        }

        // Rebote Vertical (Ejes Y)
        if (nuevoY + altoBoton > altoVentana || nuevoY < 0) {
            dirY = -dirY; // Invierte la dirección            
            nuevoY = y + dirY * velocidadActual;
        }

        jBclick.setLocation(nuevoX, nuevoY);
    }
    
    
    
    
    
    
