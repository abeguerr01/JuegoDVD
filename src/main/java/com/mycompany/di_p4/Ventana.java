    private void jMIpausaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMIpausaActionPerformed
        if(pause){ //Si esta pausado
            pause = false; //Despausa
            timerVisible.start();
            jMIpausa.setText("Pausar");
        }
        else if (!pause){ //Si NO esta pausado
            pause = true; //Pausa
            timerVisible.stop();
            jMIpausa.setText("Reanudar");
        }
        
    }//GEN-LAST:event_jMIpausaActionPerformed

    private void jBclickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBclickActionPerformed
        if(pause){ //Si esta en pausa no funciona
            return;
        }
        if (!timer.isRunning()) { //El timer no empieza hasta el primer click
            timer.start();
            timerVisible.start();
            return;
        }
        
        puntuacion++;
        
        jMpuntuacion.setText("Puntuación: "+puntuacion);
    }//GEN-LAST:event_jBclickActionPerformed

    
    
